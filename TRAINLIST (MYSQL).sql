CREATE TABLE CITYSTATION
(
STATION_ID VARCHAR(4) NOT NULL,
CITY VARCHAR(40),
PRIMARY KEY(STATION_ID)
);

CREATE TABLE TRAIN_LIST
(
TRAIN_NO INT NOT NULL,
SOURCE VARCHAR(4),
DESTINATION VARCHAR(4),
DISTANCE INT,
DEPARTS VARCHAR(6),
ARRIVES VARCHAR(6),
TRAVELTIME VARCHAR(6),
PRIMARY KEY(TRAIN_NO),
FOREIGN KEY(SOURCE) REFERENCES CITYSTATION(STATION_ID),
FOREIGN KEY(DESTINATION) REFERENCES CITYSTATION(STATION_ID)
);

CREATE TABLE CURRENT_STATUS
(
TRAIN_NO INT NOT NULL,
SLEEPER VARCHAR(10),
AC3 VARCHAR(10),
AC2 VARCHAR(10),
AC1 VARCHAR(10),
FOREIGN KEY(TRAIN_NO) REFERENCES TRAIN_LIST(TRAIN_NO)
);

CREATE TABLE TRAIN
(
TRAIN_NO INT NOT NULL,
TRAIN_NAME VARCHAR(100),
TRAIN_TYPE VARCHAR(20),
SPEED VARCHAR(15),
DAYS VARCHAR(7),
FOREIGN KEY(TRAIN_NO) REFERENCES TRAIN_LIST(TRAIN_NO)
);


CREATE TABLE PRICE
(
TRAIN_NO INT NOT NULL,
PSLEEPER INT,
PAC3 INT,
PAC2 INT,
PAC1 INT,
FOREIGN KEY(TRAIN_NO) REFERENCES TRAIN_LIST(TRAIN_NO)
);

CREATE TABLE TRAIN_ATTRIBUTES
(
TRAIN_NO INT NOT NULL,
CLEANLINESS INT,
SAFETY INT,
PUNCTUALITY INT,
FOOD VARCHAR(5),
FOREIGN KEY(TRAIN_NO) REFERENCES TRAIN_LIST(TRAIN_NO)
);


